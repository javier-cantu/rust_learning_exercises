// Exercise 001 - Hello World

// The `main` function is the entry point of a Rust program. 
fn main() {
    // The `println!` macro prints text to the console.
    // Macros in Rust end with an exclamation mark `!`.
    // Here, `println!` takes a string as an argument and prints it, followed by a newline.
    println!("Exercise 001");
    println!("Hello World!");
}
